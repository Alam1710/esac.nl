<p>De ESAC hecht veel waarde aan de bescherming van uw persoonsgegevens. In deze Privacy Policy willen we heldere en transparante informatie geven over hoe wij omgaan met persoonsgegevens.</p>
<p>Wij doen er alles aan om uw privacy te waarborgen en gaan daarom zorgvuldig om met persoonsgegevens. De ESAC houdt zich in alle gevallen aan de toepasselijke wet- en regelgeving, waaronder de Algemene Verordening Gegevensbescherming. &nbsp;Dit brengt met zich mee dat wij in ieder geval:<br></p>
<ul>
  <li>
    <p>Uw persoonsgegevens verwerken in overeenstemming met het doel waarvoor deze zijn verstrekt, deze doelen en type persoonsgegevens zijn beschreven in deze Privacy policy;</p>
  </li>
  <li>
    <p>Verwerking van uw persoonsgegevens beperken tot enkel die gegevens welke minimaal nodig zijn voor de doeleinden waarvoor ze worden verwerkt;</p>
  </li>
  <li>
    <p>Vragen om uw uitdrukkelijke toestemming als wij deze nodig hebben voor de verwerking van uw persoonsgegevens;</p>
  </li>
  <li>
    <p>Passende technische en organisatorische maatregelen hebben genomen zodat de beveiliging van uw persoonsgegevens gewaarborgd is;</p>
  </li>
  <li>
    <p>Geen persoonsgegevens doorgeven aan andere partijen, tenzij dit nodig is voor uitvoering van de doeleinden waarvoor ze zijn verstrekt;</p>
  </li>
  <li>
    <p>Op de hoogte zijn van uw rechten omtrent uw persoonsgegevens, u hierop willen wijzen en deze respecteren.</p>
  </li>
</ul>
<p>Als ESAC zijn wij verantwoordelijk voor de verwerking van uw persoonsgegevens. Indien u na het doornemen van onze Privacy policy, of in algemenere zin, vragen heeft hierover of contact met ons wenst op te nemen kan dit via de contactgegevens onderaan dit document.</p><p><br></p>
<p><b>Welke persoonsgegevens</b></p>
<p>De persoonsgegevens die wij als ESAC verzamelen kunnen ingedeeld worden in meerdere categorieën:</p>
<ol>
  <li>
    <p>Ledengegevens: Hieronder vallen alle gegevens die door leden ingevuld worden op het inschrijfformulier of in het accountoverzicht op de website, alsook de datum van inschrijving en uitschrijving.</p>
  </li>
  <li>
    <p>Financiële gegevens: Hieronder vallen alle gegevens die nodig zijn voor het correct bijhouden van de financiën.</p>
  </li>
  <li>
    <p>Gegevens voor activiteiten en trainingen: Hieronder vallen alle gegevens die door leden worden ingevuld op het inschrijfformulier van de activiteit/training, alsook de datum van inschrijving en uitschrijving voor die activiteit/training.</p>
  </li>
  <li>
    <p>Statussen: Hieronder vallen alle gegevens over de door een lid, al dan niet in het verleden, behaalde klim statussen, kader statussen en bergsport statussen, alsook de vorderingen in het traject naar een status.</p>
  </li>
  <li>
    <p>Foto’s en verhalen: Hieronder vallen alle foto’s gemaakt of verhalen verteld over bijeenkomsten vallend onder de ESAC.</p><p><br></p>
  </li>
</ol>
<p><b>Doeleinden persoonsgegevens</b></p>
<p>De persoonsgegevens die wij als ESAC verzamelen dienen verschillende doelen:</p>
<ol>
  <li>
    <p>Ledengegevens: Het doel van het bijhouden van ledengegevens is het intern functioneren van de ESAC en de communicatie met haar leden. Daarnaast worden naam, mobielnummer en emailadres intern gedeeld ten behoeve van communicatie.</p>
  </li>
  <li>
    <p>Financiële gegevens: Het doel van het bijhouden van financiële gegevens is het correct kunnen bijhouden van de financiën.</p>
  </li>
  <li>
    <p>Gegevens voor activiteiten en trainingen: Het doel van het bijhouden van gegevens voor activiteiten en trainingen is het probleemloos kunnen organiseren van deze activiteiten en trainingen.</p>
  </li>
  <li>
    <p>Statussen: Het doel van het bijhouden van statussen is ervoor te zorgen dat wanneer er bij de ESAC geklommen wordt, dit veilig en volgens de in klimgebieden en hallen geldende regels gebeurd.</p>
  </li>
  <li>
    <p>Foto’s en verhalen: Het doel van het bijhouden van foto’s en verhalen is het plaatsen van deze gegevens op media zoals de Musketon en de website zowel als promotie voor de ESAC en komende activiteiten, als om zij die deel hebben genomen aan deze activiteit de gelegenheid te geven foto’s hiervan te kunnen bekijken.</p>
    <p><br></p>
  </li>
</ol>
<p><b>Toegang tot persoonsgegevens</b></p>
<p>De persoonsgegevens die wij als ESAC verzamelen kunnen worden ingezien door verschillende (groepen) mensen:</p>
<ol>
  <li>
    <p>Ledengegevens: Deze gegevens kunnen worden ingezien door het bestuur en de internetcommissie (IC).</p>
  </li>
  <li>
    <p>Financiële gegevens: Deze gegevens kunnen worden ingezien door het bestuur en de kascontrolecommissie (KCC).</p>
  </li>
  <li>
    <p>Gegevens voor activiteiten en trainingen: Deze gegevens kunnen worden ingezien door activiteiten organiserende commissies het bestuur en de IC. Het feit dat je deelneemt aan een activiteit kan worden ingezien door alle ESAC leden.</p>
  </li>
  <li>
    <p>Statussen: Deze gegevens kunnen worden ingezien door het bestuur, de IC en de opleidingscommissie (OC). Wanneer je jezelf inschrijft voor een weekend kan je status ingezien worden door alle leden van de ESAC.</p>
  </li>
  <li>
    <p>Foto’s en verhalen: Deze gegevens kunnen op media worden geplaatst die voor iedereen toegankelijk zijn en zo dus door iedereen worden ingezien.</p>
  </li>
</ol>
<p>Om te garanderen dat uw persoonsgegevens vertrouwelijk worden behandeld zijn zowel het bestuur als leden van de IC verplicht een geheimhoudingsverklaring te tekenen voordat zij kunnen toetreden tot het bestuur dan wel de IC respectievelijk. Deze geheimhoudingsverklaring kan <a href="https://drive.google.com/drive/u/0/folders/1p8y9rumkiV4fEfmm1D_9cbEkjnUVbZTC">hier</a> worden ingezien.</p><p><br></p>
<p><b>Verstrekking aan derden</b></p>
<p>De gegevens die u aan ons geeft kunnen wij aan derde partijen verstrekken indien dit noodzakelijk is voor het doel waarvoor ze zijn verstrekt. Dit omvat onder andere maar is niet beperkt tot:</p>
<ol>
  <li>
    <p>Ledenlijst SSC: Het SSC regelt verschillende kortingen voor ESAC leden. Om dit te kunnen doen hebben zij toegang tot een ledenlijst van de ESAC nodig. Op deze ledenlijst staan de volledige naam, lidstatus, sportkaartnummer en indien van toepassing bestuursfunctie of trainersfunctie van alle leden van de ESAC. Het SSC zelf koppelt dit aan het corresponderende SSC abonnement.</p>
  </li>
  <li>
    <p>Sleutellijsten SSC: Om te kunnen bepalen wie er toegang hebben tot verschillende faciliteiten van de ESAC op het SSC deelt de ESAC per faciliteit een sleutellijst met het SSC. Op deze lijst staan de voor- en achternaam van iedereen die toegang heeft tot deze faciliteit als ook de functie die deze persoon het recht geeft om toegang te hebben tot deze faciliteit. Op deze sleutellijsten staan in ieder geval geen andere personen dan bestuur en/of kader van de ESAC.</p>
  </li>
  <li>
    <p>Ledenlijst Monk: Leden van de ESAC hebben soms korting bij Monk. Om te kunnen bepalen wie er recht hebben op deze korting deelt de ESAC een ledenlijst met Monk. Op deze lijst staat de volledige naam van alle leden van de ESAC.</p>
  </li>
  <li>
    <p>Noodgegevens NSAC: De NSAC noodprocedure wordt ingezet in het geval dat een SAC lid betrokken raakt bij een ongeval in de klim- of bergsport. Om ervoor te zorgen dat deze procedure in zo’n geval voorspoedig kan verlopen heeft de NSAC de noodgegevens van alle ESAC leden nodig.</p>
  </li>
  <li>
    <p>Google: Binnen de ESAC wordt &nbsp;gebruik gemaakt van een aantal van de diensten van Google. Zo wordt Google Drive gebruikt voor het verwerken van bepaalde gegevens en wordt Gmail gebruikt voor de mailaccounts van het bestuur en de commissies. Er zal altijd naar gestreefd worden zo min mogelijk persoonsgegevens hier te verwerken.</p>
    <p><br></p>
  </li>
</ol>
<p><b>Bewaartermijn</b></p>
<p>ESAC bewaart persoonsgegevens niet langer dan noodzakelijk voor het doel waarvoor deze zijn verstrekt dan wel op grond van de wet is vereist.</p>
<ol>
  <li>
    <p>Ledengegevens: Deze gegevens zullen binnen twee jaar na opzegging van het lidmaatschap worden verwijderd.</p>
  </li>
  <li>
    <p>Financiële gegevens: Deze gegevens zullen worden verwijderd nadat alle openstaande schulden zijn afgehandeld en de wettelijke bewaartermijn van zeven jaar is verstreken.</p>
  </li>
  <li>
    <p>Gegevens voor activiteiten en trainingen: Deze gegevens zullen worden verwijderd binnen twee jaar nadat de complete activiteit of training, inclusief de afrekening, is afgerond.</p>
  </li>
  <li>
    <p>Statussen: Deze gegevens zullen binnen twee jaar na opzegging van het lidmaatschap worden verwijderd.</p>
  </li>
  <li>
    <p>Foto’s en verhalen: Deze gegevens zullen alleen op aanvraag verwijderd worden. In het geval dat deze gegevens na toestemming gedeeld zijn op media zou het kunnen zijn dat het door de aard van het media niet meer mogelijk is de gegevens compleet te verwijderen. In een dergelijk geval zullen de gegevens voor zover als realistisch mogelijk verwijderd worden.</p>
    <p><br></p>
  </li>
</ol>
<p><b>Beveiliging</b></p>
<p>Wij hebben passende technische en organisatorische maatregelen genomen om persoonsgegevens van u te beschermen tegen onrechtmatige verwerking.</p>
<p>Wanneer het bestuur of de IC op de hoogte is van een datalek bij ons of bij derden zullen de leden zo spoedig mogelijk op de hoogte worden gebracht van het datalek.</p>
<p><br></p>
<p><b>Rechten omtrent uw gegevens</b></p>
<p>U heeft recht op inzage, rectificatie of verwijdering van de persoonsgegeven welke wij van u ontvangen hebben. U kunt uw gegevens inzien op de website van de ESAC (esac.nl). Tevens kunt u bezwaar maken tegen de verwerking van uw persoonsgegevens (of een deel hiervan) door het bestuur of leden van commissies. Ook heeft u het recht om de door u verstrekte gegevens door ons te laten overdragen aan uzelf of in opdracht van u direct aan een andere partij. Wij kunnen u vragen om u te legitimeren voordat wij gehoor kunnen geven aan voornoemde verzoeken.<br>
<br>
Mogen wij uw persoonsgegevens verwerken op basis van een door u gegeven toestemming hiertoe, dan heeft u altijd het recht deze toestemming in te trekken. Het is dan wel mogelijk dat verdere lidmaatschap bij de ESAC of deelname aan bepaalde activiteiten of diensten dan niet meer mogelijk is.</p>
